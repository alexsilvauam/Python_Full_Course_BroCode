#Input() function allows you to get input from the user
#The input() function takes a string as an argument, which is the prompt to the user
#The input() function returns the user's input as a string
#The input() function can be used to get input from the user in a loop
#The input() function can be used to get input from the user in a function
#The input() function can be used to get input from the user in a class

#Example:
name = input("What is your name: ")
age = int(input("How old are you:"))
print("Hello, " + name)
print("You are " + age + " years old")
#or 

print (f"Hello, {name}")
print (f"You are {age} years old")
